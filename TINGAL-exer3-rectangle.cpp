#include <stdio.h>

int main()
{
	int l, w, area, perimeter;
	printf("Enter the length of the rectangle: ");
	scanf("%d", &l);
	printf("Enter the width of the rectangle: ");
	scanf("%d", &w);
	
	area = l * w;
	perimeter = 2 * l + 2 * w;
	
	printf("The area of the square is: %d\nThe perimeter of the square is: %d", area, perimeter);
	
	return 0;
}

