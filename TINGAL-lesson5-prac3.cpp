#include <stdio.h>

int fact(int i) {
    int result = 1;
    for(int x = 1; x <= i; ++x) {
        result *= x;
    }
    return result;
}

int main() {
    int numb1;
    printf("Enter a number: ");
    scanf("%d", &numb1);

    printf("The factorial of %d is %d\n", numb1, fact(numb1));

    return 0;
}

